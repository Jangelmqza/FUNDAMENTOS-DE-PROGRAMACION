#Programa que convierte de metros a mm, km y cm
print("Programa que convierte de metros a milimetros, kilometros y centimetros\n")

n1=float(input("Proporcione la cantidad de metros que desea convertir: "))

MM=n1*1000
CM=n1*100
KM=n1*0.001

print(f"\nEn centimetros es {CM}\n")
print(f"En milimetros es {MM}\n")
print(f"En kilometros es {KM}\n")

